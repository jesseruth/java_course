package uw.cp510.lectures.lecture2;

public class ForLoops {
    public static void main(String[] args) {
        for (int x = 100; x >= 1; x--) {
            System.out.println(x);
        }
        int y = 100;
        while (y >= 1) {
            System.out.println(y);
            y--;
        }
    }
}
